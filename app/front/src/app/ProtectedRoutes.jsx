import { useContext } from "react";
import { Navigate, Outlet } from "react-router-dom";
import { AuthContext } from "../context/AuthContext";

const ProtectedRoute = ({ requiredRole }) => {
  const { user, loading } = useContext(AuthContext);

  // estoy previene que el componente se renderice antes de tiempo y te expulse al login
  if (loading) return <div>Loading...</div>;

  if (!user) return <Navigate to="/login" />;

  // Lógica de jerarquía: Superuser entra a todo
  if (user.isSuperuser) return <Outlet />;

  // Si se requiere ser Admin y no lo es
  if (requiredRole === "admin" && !user.isAdmin) {
    return <Navigate to="/dashboard" />;
  }

  // Si se requiere ser Superuser y no lo es (ya validado arriba que no es superuser)
  if (requiredRole === "superuser") {
    return <Navigate to="/dashboard" />;
  }

  return <Outlet />;
};

export default ProtectedRoute;